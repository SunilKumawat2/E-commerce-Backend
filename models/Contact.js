const mongoose = require("mongoose");

const ContactSchame = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    mobile: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    }
})

const ContactModal = mongoose.model("contacts", ContactSchame)
module.exports = ContactModal;